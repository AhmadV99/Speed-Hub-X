	spawn(function()
		pcall(function()
			loadstring([[
				local RS = game:GetService("RunService").RenderStepped
				local function GetFPS() 
					RS:Wait() 
					local V = 0 for _ = 1, 5 do 
						V += 1 / RS:Wait() 
					end 
					return math.round(V / 5) 
				end
				pcall(function() setfpscap(GetFPS()) end)
			]])()
		end)
	end)
end

